#include "ft_tail.h"

unsigned int	ft_atoui(char *str)
{
	unsigned int	n;

	n = 0;
	while (ft_isdigit(*str))
	{
		n *= 10;
		n += *str - '0';
		++str;
	}
	return (n);
}

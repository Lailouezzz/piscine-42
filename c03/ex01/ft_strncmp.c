int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	k;

	if (n == 0)
		return (0);
	k = 0;
	while (*s1 == *s2 && *s1 != '\0' && *s2 != '\0' && k < n - 1)
	{
		++s1;
		++s2;
		++k;
	}
	return (*s1 - *s2);
}

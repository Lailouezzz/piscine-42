#include <stdio.h>

char	*ft_strupcase(char *str)
{
	char	*p;

	p = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str += 'A' - 'a';
		}
		++str;
	}
	return (p);
}

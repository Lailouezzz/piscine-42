#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	char	*p;

	p = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str += 'a' - 'A';
		}
		++str;
	}
	return (p);
}

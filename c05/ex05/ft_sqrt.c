int	ft_sqrt(int nb)
{
	int	sq;

	if (nb < 0)
		return (0);
	sq = 1;
	while (sq <= nb / sq)
	{
		if (sq * sq == nb)
			return (sq);
		++sq;
	}
	return (0);
}

#include "ft.h"

/* return : -1 if EOF or error | the char */
int	ft_getchar(int fd)
{
	char	c;
	ssize_t	r;

	r = read(fd, &c, 1);
	if (r != 1)
		return (-1);
	return ((int)c);
}

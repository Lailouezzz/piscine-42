void	ft_swap(int *a, int *b)
{
	int	t;

	t = *a;
	*a = *b;
	*b = t;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	k;

	k = 0;
	while (k < size / 2)
	{
		ft_swap(tab + k, tab + size - k - 1);
		++k;
	}
}
